#include<bits/stdc++.h>
using namespace std;


struct hello
{

    int a;
    char b;
    float c;
};

int main()
{

    struct hello g;

    g.a = 10;
    g.b = 'a';
    g.c = 32.3;


    cout<<"The value is : "<<g.a<<endl;
    cout<<"The value is : "<<g.b<<endl;
    cout<<"The value is : "<<g.c<<endl;


}
